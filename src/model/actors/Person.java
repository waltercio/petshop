package model.actors;


public class Person {

	private String name = null;
	private String address = null;
	private String phone = null;
	private String gender = null;
	private String cpf = null;

	public Person() {

	}

	//get name attribute from Person class
	public String getName() {
		return name;
	}

	//set name attribute from Person class
	public void setName(String name) {
		this.name = name;
	}

	//get address attribute from Person class
	public String getAddress() {
		return address;
	}

	//set address attribute from Person class
	public void setAddress(String address) {
		this.address = address;
	}

	//get phone attribute from Person class
	public String getPhone() {
		return phone;
	}

	//set phone attribute from Person class
	public void setPhone(String phone) {
		this.phone = phone;
	}

	//get gender attribute from Person class
	public String getGender() {
		return gender;
	}

	//set gender attribute from Person class
	public void setGender(String gender) {
		this.gender = gender;
	}

	//get cpf attribute from Person class
	public String getCpf() {
		return cpf;
	}

	//set cpf attribute from Person class
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}

}
