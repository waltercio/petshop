package model.actors;

public class User {

	private String name = null;
	private String username = null;
	private String password = null;

	public User() {

	}

	//get name attribute from User class
	public String getName() {
		return name;
	}

	//set name attribute from User class
	public void setName(String name) {
		this.name = name;
	}

	//get username attribute from User class
	public String getUsername() {
		return username;
	}

	//set username attribute from User class
	public void setUsername(String username) {
		this.username = username;
	}

	//get password attribute from User class
	public String getPassword() {
		return password;
	}

	//set password attribute from User class
	public void setPassword(String password) {
		this.password = password;
	}

}